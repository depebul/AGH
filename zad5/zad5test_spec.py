from testy import MY_random

def my_randint(a,b):
  return a+MY_random()%(b-a+1)

def gentest(test, arg, hint ):
  if test<6:
    return arg,hint
  
  n = test
  e = 3*n
  t = set()
  E = []
  for _ in range(e):
    u = my_randint(0,n-1)    
    v = my_randint(0,n-1)    
    if u==v: continue
    if u>v: u,v = v,u
    if (u,v) in t: continue
    t.add((u,v))
    E.append((u,v,my_randint(10,99)))
  
  S = []
  for _ in range(n//10):
    u = my_randint(0,n-1)   
    if u in S: continue
    S.append(u)

  a = my_randint(0,n-1)   
  while True:
    b = my_randint(0,n-1)   
    if b!=a: break

  return [n,E,S,a,b],hint
   

  
